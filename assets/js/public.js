jQuery(document).ready(function($) {
    let selectedCategory = null;
    let selectedSubCategory = null;
    let currentStep = 1;
    let productPrice = 0;
    let currentCartKey = null;
    
    // Buy Only Frame button
    $(document).on('click', '#buy-only-frame-button', function(e) {
        e.preventDefault();
        const productId = $(this).data('product-id');
        const $button = $(this);
        
        // Disable button
        $button.prop('disabled', true).text('Adding...');
        
        // Add to cart using WooCommerce AJAX
        $.ajax({
            url: wc_add_to_cart_params.wc_ajax_url.toString().replace( '%%endpoint%%', 'add_to_cart' ),
            type: 'POST',
            data: {
                product_id: productId,
                quantity: 1
            },
            success: function(response) {
                if (response.error && response.product_url) {
                    window.location = response.product_url;
                    return;
                }
                
                // Redirect to cart
                window.location.href = lens_zone_params.cart_url;
            },
            error: function() {
                lensZoneAlert('Failed to add to cart. Please try again.', 'error', 'Error');
                $button.prop('disabled', false).text('BUY ONLY FRAME');
            }
        });
    });

    // Open modal
    $(document).on('click', '#select-lens-button', function(e) {
        e.preventDefault();
        console.log('Select Lens Button Clicked');
        
        const productId = $(this).data('product-id');
        console.log('Product ID:', productId);
        
        // Don't get price from DOM here - we'll get it from the modal footer after it loads
        productPrice = 0;
        
        $('#lens-selection-modal').addClass('active');
        loadLensCategories(productId);
    });
    


    // Close modal
    $(document).on('click', '.close, #lens-selection-modal', function(e) {
        if (e.target === this) {
            $('#lens-selection-modal').removeClass('active');
            resetModal();
        }
    });

    // Load lens categories
    function loadLensCategories(productId) {
        console.log('Loading categories for product:', productId);
        console.log('AJAX URL:', lens_zone_params.ajax_url);
        
        $.ajax({
            url: lens_zone_params.ajax_url,
            type: 'POST',
            data: {
                action: 'load_lens_categories',
                nonce: lens_zone_params.nonce,
                product_id: productId
            },
            beforeSend: function() {
                $('#modal-body').html('<div style="text-align:center;padding:40px;">Loading...</div>');
            },
            success: function(response) {
                console.log('AJAX Response:', response);
                if (response.success) {
                    $('#modal-body').html(response.data);
                    updateProgress(1);
                    
                    // Get the correct product price from the modal footer (generated by PHP)
                    const footerPriceText = $('.modal-footer .subtotal strong').text();
                    productPrice = parseFloat(footerPriceText.replace(/[^0-9.]/g, '')) || 0;
                    console.log('Product Price from modal:', productPrice);
                } else {
                    console.error('AJAX Error:', response);
                    $('#modal-body').html('<div style="text-align:center;padding:40px;color:red;">Error loading categories</div>');
                }
            },
            error: function(xhr, status, error) {
                console.error('AJAX Request Failed:', status, error);
                $('#modal-body').html('<div style="text-align:center;padding:40px;color:red;">Request failed: ' + error + '</div>');
            }
        });
    }

    // Select lens category
    $(document).on('click', '.lens-card[data-category-id]', function() {
        $('.lens-card').removeClass('selected');
        $(this).addClass('selected');
        selectedCategory = $(this).data('category-id');
        
        // Instant auto-advance to next step (no continue button on first screen)
        loadLensSubCategories(selectedCategory);
    });

    // Continue from category selection
    $(document).on('click', '.continue-from-category', function() {
        if (!selectedCategory) {
            lensZoneAlert('Please select a lens category first', 'warning', 'Selection Required');
            return;
        }
        loadLensSubCategories(selectedCategory);
    });

    // Load lens sub-categories
    function loadLensSubCategories(categoryId) {
        $.ajax({
            url: lens_zone_params.ajax_url,
            type: 'POST',
            data: {
                action: 'load_lens_sub_categories',
                nonce: lens_zone_params.nonce,
                category_id: categoryId
            },
            beforeSend: function() {
                // Show loading state
                $('#modal-body').html('<div style="text-align:center;padding:40px;"><div class="spinner" style="border:3px solid #f3f3f3;border-top:3px solid #1e3a8a;border-radius:50%;width:40px;height:40px;animation:spin 1s linear infinite;margin:0 auto;"></div></div>');
            },
            success: function(response) {
                if (response.success) {
                    // Check if we should skip to cart (no sub-categories)
                    if (response.data.skip_to_power) {
                        // No sub-categories, add product to cart WITHOUT lens data
                        const productId = $('#select-lens-button').data('product-id');
                        
                        // Use WooCommerce's add to cart
                        $.ajax({
                            url: '?add-to-cart=' + productId,
                            type: 'GET',
                            success: function() {
                                // Close modal and redirect to cart
                                $('#lens-selection-modal').removeClass('active');
                                window.location.href = lens_zone_params.cart_url;
                            }
                        });
                    } else {
                        // Normal flow - show sub-categories
                        $('#modal-body').html(response.data);
                        updateProgress(2);
                    }
                }
            }
        });
    }

    // View more/less points
    $(document).on('click', '.view-more-btn', function() {
        const $points = $(this).siblings('.lens-card-points');
        const $hiddenPoints = $points.find('li:hidden');
        
        if ($hiddenPoints.length > 0) {
            $hiddenPoints.show();
            $(this).text('View Less');
        } else {
            $points.find('li:gt(2)').hide();
            $(this).text('View More');
        }
    });

    // Select lens sub-category
    $(document).on('click', '.lens-card[data-subcategory-id]', function() {
        $('.lens-card').removeClass('selected');
        $(this).addClass('selected');
        selectedSubCategory = $(this).data('subcategory-id');
        const price = parseFloat($(this).data('price')) || 0;
        updateSubtotal(productPrice + price);
        
        // Enable continue button (NO auto-advance on this screen)
        $('.continue-from-subcategory').prop('disabled', false);
    });

    // Continue from sub-category selection
    $(document).on('click', '.continue-from-subcategory', function() {
        if (!selectedSubCategory) {
            lensZoneAlert('Please select a lens sub-category first', 'warning', 'Selection Required');
            return;
        }
        loadPowerOptions();
    });

    // Load power options
    function loadPowerOptions() {
        $.ajax({
            url: lens_zone_params.ajax_url,
            type: 'POST',
            data: {
                action: 'load_power_options',
                nonce: lens_zone_params.nonce
            },
            success: function(response) {
                if (response.success) {
                    $('#modal-body').html(response.data);
                    updateProgress(3);
                }
            }
        });
    }

    // Select power option
    $(document).on('click', '.power-option-card', function() {
        $('.power-option-card').removeClass('selected');
        $(this).addClass('selected');
        
        const option = $(this).data('option');
        
        if (option === 'submit-later') {
            $('.continue-button').text('Continue').addClass('submit-later-btn');
            // Instant auto-advance for submit-later
            proceedToCheckout('submit-later');
        } else if (option === 'manual') {
            $('.continue-button').text('Continue').removeClass('submit-later-btn');
            // Instant auto-advance to manual form
            loadManualPowerFormScreen();
        } else if (option === 'upload') {
            $('.continue-button').text('Continue').removeClass('submit-later-btn');
            // Instant auto-advance to upload form
            loadUploadFormScreen();
        }
    });
    
    // Continue button from power options page
    $(document).on('click', '.continue-button:not(.save-proceed-button):not(.upload-proceed-button)', function(e) {
        e.preventDefault();
        console.log('Continue button clicked');
        
        if ($(this).hasClass('submit-later-btn')) {
            // Submit later - go to cart
            console.log('Submit later selected');
            proceedToCheckout('submit-later');
        } else {
            // Check which option is selected
            const selectedOption = $('.power-option-card.selected').data('option');
            console.log('Selected option:', selectedOption);
            
            if (selectedOption === 'manual') {
                // Load manual form in new screen
                loadManualPowerFormScreen();
            } else if (selectedOption === 'upload') {
                // Load upload form in new screen
                loadUploadFormScreen();
            }
        }
    });

    // Load manual power form in new screen
    function loadManualPowerFormScreen() {
        console.log('Loading manual power form screen');
        $.ajax({
            url: lens_zone_params.ajax_url,
            type: 'POST',
            data: {
                action: 'load_manual_power_form',
                nonce: lens_zone_params.nonce,
                category_id: selectedCategory
            },
            beforeSend: function() {
                $('#modal-body').html('<div style="text-align:center;padding:40px;">Loading...</div>');
            },
            success: function(response) {
                console.log('Manual form response:', response);
                if (response.success) {
                    $('#modal-body').html(response.data);
                }
            }
        });
    }
    
    // Load upload form in new screen
    function loadUploadFormScreen() {
        console.log('Loading upload form screen');
        $.ajax({
            url: lens_zone_params.ajax_url,
            type: 'POST',
            data: {
                action: 'load_upload_form',
                nonce: lens_zone_params.nonce
            },
            beforeSend: function() {
                $('#modal-body').html('<div style="text-align:center;padding:40px;">Loading...</div>');
            },
            success: function(response) {
                console.log('Upload form response:', response);
                if (response.success) {
                    $('#modal-body').html(response.data);
                    // Initialize upload handlers after form is loaded
                    initializeUploadHandlers();
                }
            }
        });
    }
    
    // Initialize upload handlers
    function initializeUploadHandlers() {
        console.log('Initializing upload handlers');
        
        // Upload area click - use event delegation
        $(document).off('click', '#prescription-upload-area').on('click', '#prescription-upload-area', function(e) {
            e.preventDefault();
            e.stopPropagation();
            console.log('Upload area clicked');
            $('#prescription-file-input').trigger('click');
        });
        
        // File input change
        $(document).off('change', '#prescription-file-input').on('change', '#prescription-file-input', function(e) {
            const file = this.files[0];
            console.log('File selected:', file);
            
            if (file) {
                // Check file size (5MB max)
                if (file.size > 5 * 1024 * 1024) {
                    lensZoneAlert('File size must be under 5MB', 'warning', 'File Too Large');
                    $(this).val('');
                    return;
                }
                
                // Upload file via AJAX
                const formData = new FormData();
                formData.append('action', 'upload_prescription_file');
                formData.append('nonce', lens_zone_params.nonce);
                formData.append('prescription_file', file);
                
                $.ajax({
                    url: lens_zone_params.ajax_url,
                    type: 'POST',
                    data: formData,
                    processData: false,
                    contentType: false,
                    beforeSend: function() {
                        $('#prescription-upload-area').html('<div style="text-align:center;padding:40px;">Uploading...</div>');
                    },
                    success: function(response) {
                        console.log('Upload response:', response);
                        if (response.success) {
                            $('#uploaded-filename').text(file.name);
                            $('#upload-preview').show();
                            $('#prescription-upload-area').hide();
                            $('.upload-proceed-button').prop('disabled', false).data('file-url', response.data.url);
                        } else {
                            lensZoneAlert('Upload failed: ' + response.data, 'error', 'Upload Error');
                            $('#prescription-upload-area').html('<div class="upload-area-icon">📄</div><div class="upload-area-text">Tap here to upload prescription image</div><div class="upload-area-subtext">(Max. size: 5MB)</div>');
                        }
                    },
                    error: function(xhr, status, error) {
                        console.error('Upload error:', error);
                        lensZoneAlert('Upload failed. Please try again.', 'error', 'Upload Error');
                        $('#prescription-upload-area').html('<div class="upload-area-icon">📄</div><div class="upload-area-text">Tap here to upload prescription image</div><div class="upload-area-subtext">(Max. size: 5MB)</div>');
                    }
                });
            }
        });
        
        // Upload proceed button
        $(document).off('click', '.upload-proceed-button').on('click', '.upload-proceed-button', function(e) {
            e.preventDefault();
            const fileUrl = $(this).data('file-url');
            
            if (!fileUrl) {
                lensZoneAlert('Please upload a prescription file', 'warning', 'File Required');
                return;
            }
            
            // Validate required fields
            const userName = $('#upload_user_name').val().trim();
            const userPhone = $('#upload_user_phone').val().trim();
            
            if (!userName || !userPhone) {
                lensZoneAlert('Please fill in your name and phone number', 'warning', 'Information Required');
                return;
            }
            
            // Proceed to checkout with upload data
            const lensData = {
                power_type: 'upload',
                prescription_url: fileUrl,
                user_name: userName,
                user_phone: userPhone
            };
            
            // Add to cart and redirect
            if (typeof window.proceedToCheckoutWithUpload === 'function') {
                window.proceedToCheckoutWithUpload(lensData);
            } else {
                console.error('proceedToCheckoutWithUpload function not found');
            }
        });
    }
    
    // Proceed to checkout
    function proceedToCheckout(powerType) {
        console.log('Proceeding to checkout with:', powerType);
        console.log('Selected category:', selectedCategory);
        console.log('Selected subcategory:', selectedSubCategory);
        
        // Category is required, but sub-category is optional (can be 0)
        if (!selectedCategory) {
            lensZoneAlert('Please select lens category first', 'warning', 'Selection Required');
            return;
        }
        
        const lensData = {
            category_id: selectedCategory,
            subcategory_id: selectedSubCategory || 0, // 0 if no sub-category
            power_type: powerType
        };
        
        // If manual power, collect form data
        if (powerType === 'manual') {
            // Validate required fields
            const userName = $('input[name="user_name"]').val().trim();
            const userPhone = $('input[name="user_phone"]').val().trim();
            
            if (!userName || !userPhone) {
                lensZoneAlert('Please fill in your name and phone number', 'warning', 'Information Required');
                return;
            }
            
            lensData.left_sph = $('select[name="left_sph"]').val();
            lensData.left_cyl = $('select[name="left_cyl"]').val();
            lensData.left_axis = $('input[name="left_axis"]').val();
            lensData.left_additional = $('select[name="left_additional"]').val();
            lensData.right_sph = $('select[name="right_sph"]').val();
            lensData.right_cyl = $('select[name="right_cyl"]').val();
            lensData.right_axis = $('input[name="right_axis"]').val();
            lensData.right_additional = $('select[name="right_additional"]').val();
            lensData.same_power = $('#same-power-checkbox').is(':checked');
            lensData.user_name = userName;
            lensData.user_phone = userPhone;
        }
        
        console.log('Sending lens data:', lensData);
        
        $.ajax({
            url: lens_zone_params.ajax_url,
            type: 'POST',
            data: {
                action: 'add_lens_to_cart',
                nonce: lens_zone_params.nonce,
                product_id: $('#select-lens-button').data('product-id'),
                lens_data: lensData
            },
            beforeSend: function() {
                $('.continue-button, .save-proceed-button').prop('disabled', true).text('Adding...');
            },
            success: function(response) {
                console.log('Add to cart response:', response);
                if (response.success) {
                    console.log('Lens price added:', response.data.lens_price);
                    console.log('Redirecting to cart...');
                    window.location.href = lens_zone_params.cart_url;
                } else {
                    lensZoneAlert('Error adding to cart: ' + (response.data || 'Unknown error'), 'error', 'Error');
                    $('.continue-button, .save-proceed-button').prop('disabled', false).text('Continue');
                }
            },
            error: function(xhr, status, error) {
                console.error('Add to cart failed:', error);
                console.error('Response:', xhr.responseText);
                lensZoneAlert('Failed to add to cart. Please try again.', 'error', 'Error');
                $('.continue-button, .save-proceed-button').prop('disabled', false).text('Continue');
            }
        });
    }
    
    // Save and proceed button
    $(document).on('click', '.save-proceed-button', function() {
        proceedToCheckout('manual');
    });

    // Same power checkbox
    $(document).on('change', '#same-power-checkbox', function() {
        if ($(this).is(':checked')) {
            $('.power-form-grid').removeClass('two-columns').addClass('one-column');
            $('.right-eye-column').hide();
        } else {
            $('.power-form-grid').removeClass('one-column').addClass('two-columns');
            $('.right-eye-column').show();
        }
    });

    // Back button
    $(document).on('click', '.back-button', function(e) {
        e.preventDefault();
        
        if (currentStep === 2) {
            loadLensCategories($('#select-lens-button').data('product-id'));
        } else if (currentStep === 3) {
            loadLensSubCategories(selectedCategory);
        }
    });

    // Update progress
    function updateProgress(step) {
        currentStep = step;
        $('.step-circle').each(function(index) {
            const stepNum = index + 1;
            if (stepNum < step) {
                $(this).addClass('completed').removeClass('active');
            } else if (stepNum === step) {
                $(this).addClass('active').removeClass('completed');
            } else {
                $(this).removeClass('active completed');
            }
        });
        
        $('.step-line').each(function(index) {
            if (index < step - 1) {
                $(this).addClass('completed');
            } else {
                $(this).removeClass('completed');
            }
        });
    }

    // Update subtotal
    function updateSubtotal(total) {
        $('.subtotal strong').text('₹' + total.toFixed(0));
    }

    // Reset modal
    function resetModal() {
        selectedCategory = null;
        selectedSubCategory = null;
        currentStep = 1;
        currentCartKey = null;
        $('#modal-body').html('');
    }
    
    // Show details popup for cart/checkout
    function showDetailsPopup(lensData, lensPrice) {
        console.log('Showing details popup');
        let detailsHtml = '<div class="modal-header">';
        detailsHtml += '<h2>Lens Details</h2>';
        detailsHtml += '<span class="close">&times;</span>';
        detailsHtml += '</div>';
        detailsHtml += '<div class="modal-body" style="padding: 24px; max-width: 600px; margin: 0 auto;">';
        
        // Power type
        let powerTypeLabel = 'Submit Later';
        if (lensData.power_type === 'manual') {
            powerTypeLabel = 'Manual Entry';
        } else if (lensData.power_type === 'upload') {
            powerTypeLabel = 'Prescription Upload';
        }
        
        detailsHtml += '<div class="detail-row"><strong>Power Type:</strong> ' + powerTypeLabel + '</div>';
        
        if (lensPrice > 0) {
            detailsHtml += '<div class="detail-row"><strong>Lens Price:</strong> ₹' + lensPrice + '</div>';
        }
        
        // Manual entry details - show in table format
        if (lensData.power_type === 'manual') {
            if (lensData.left_sph) {
                detailsHtml += '<div style="margin-top: 20px;">';
                detailsHtml += '<h3 style="margin-bottom: 12px; font-size: 16px; font-weight: 600; color: #111827;">Power Details</h3>';
                detailsHtml += '<div style="overflow-x: auto;">';
                detailsHtml += '<table style="width: 100%; border-collapse: collapse; border: 1px solid #e5e7eb; min-width: 300px;">';
                detailsHtml += '<thead><tr style="background: #f3f4f6;">';
                detailsHtml += '<th style="padding: 12px 8px; border: 1px solid #e5e7eb; text-align: left; font-weight: 600; color: #111827;">Eye</th>';
                detailsHtml += '<th style="padding: 12px 8px; border: 1px solid #e5e7eb; text-align: left; font-weight: 600; color: #111827;">SPH</th>';
                detailsHtml += '<th style="padding: 12px 8px; border: 1px solid #e5e7eb; text-align: left; font-weight: 600; color: #111827;">CYL</th>';
                detailsHtml += '<th style="padding: 12px 8px; border: 1px solid #e5e7eb; text-align: left; font-weight: 600; color: #111827;">Axis</th>';
                detailsHtml += '</tr></thead><tbody>';
                detailsHtml += '<tr>';
                detailsHtml += '<td style="padding: 12px 8px; border: 1px solid #e5e7eb; color: #374151;"><strong>Left (OS)</strong></td>';
                detailsHtml += '<td style="padding: 12px 8px; border: 1px solid #e5e7eb; color: #374151;">' + (lensData.left_sph || '-') + '</td>';
                detailsHtml += '<td style="padding: 12px 8px; border: 1px solid #e5e7eb; color: #374151;">' + (lensData.left_cyl || '-') + '</td>';
                detailsHtml += '<td style="padding: 12px 8px; border: 1px solid #e5e7eb; color: #374151;">' + (lensData.left_axis || '-') + '</td>';
                detailsHtml += '</tr>';
                detailsHtml += '<tr style="background: #f9fafb;">';
                detailsHtml += '<td style="padding: 12px 8px; border: 1px solid #e5e7eb; color: #374151;"><strong>Right (OD)</strong></td>';
                detailsHtml += '<td style="padding: 12px 8px; border: 1px solid #e5e7eb; color: #374151;">' + (lensData.right_sph || '-') + '</td>';
                detailsHtml += '<td style="padding: 12px 8px; border: 1px solid #e5e7eb; color: #374151;">' + (lensData.right_cyl || '-') + '</td>';
                detailsHtml += '<td style="padding: 12px 8px; border: 1px solid #e5e7eb; color: #374151;">' + (lensData.right_axis || '-') + '</td>';
                detailsHtml += '</tr>';
                detailsHtml += '</tbody></table>';
                detailsHtml += '</div>';
                detailsHtml += '</div>';
            }
        }
        
        // Upload details
        if (lensData.power_type === 'upload') {
            if (lensData.prescription_url) {
                detailsHtml += '<div style="margin-top: 20px; text-align: center;">';
                detailsHtml += '<a href="' + lensData.prescription_url + '" target="_blank" style="display: inline-block; padding: 12px 24px; background: #1e3a8a; color: #fff; text-decoration: none; border-radius: 8px; font-weight: 600; transition: all 0.2s ease;">📄 View Prescription</a>';
                detailsHtml += '</div>';
            }
        }
        
        detailsHtml += '</div>';
        
        $('#modal-body').html(detailsHtml);
        $('#lens-selection-modal').addClass('active');
    }
    
    // Upload prescription from cart
    $(document).on('click', '.upload-prescription-cart-btn', function() {
        loadUploadFormForCart();
    });
    
    // Load upload form for cart
    function loadUploadFormForCart() {
        $.ajax({
            url: lens_zone_params.ajax_url,
            type: 'POST',
            data: {
                action: 'load_upload_form',
                nonce: lens_zone_params.nonce
            },
            success: function(response) {
                if (response.success) {
                    $('#modal-body').html(response.data);
                    // Initialize upload handlers after form is loaded
                    initializeUploadHandlers();
                }
            }
        });
    }

    // Save and proceed button
    $(document).on('click', '.save-proceed-button', function() {
        proceedToCheckout('manual');
    });
    
    // Global function for upload proceed
    window.proceedToCheckoutWithUpload = function(uploadData) {
        const lensData = {
            category_id: selectedCategory,
            subcategory_id: selectedSubCategory,
            power_type: 'upload',
            prescription_url: uploadData.prescription_url,
            user_name: uploadData.user_name,
            user_phone: uploadData.user_phone
        };
        
        $.ajax({
            url: lens_zone_params.ajax_url,
            type: 'POST',
            data: {
                action: 'add_lens_to_cart',
                nonce: lens_zone_params.nonce,
                product_id: $('#select-lens-button').data('product-id'),
                lens_data: lensData
            },
            beforeSend: function() {
                $('.upload-proceed-button').prop('disabled', true).text('Adding to cart...');
            },
            success: function(response) {
                if (response.success) {
                    window.location.href = lens_zone_params.cart_url;
                } else {
                    lensZoneAlert('Error: ' + (response.data || 'Unknown error'), 'error', 'Error');
                    $('.upload-proceed-button').prop('disabled', false).text('Continue');
                }
            },
            error: function() {
                lensZoneAlert('Failed to add to cart. Please try again.', 'error', 'Error');
                $('.upload-proceed-button').prop('disabled', false).text('Continue');
            }
        });
    };
});
